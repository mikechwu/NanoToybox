function decodeBase64(b64: string): Uint8Array {
  let bin: string;
  try {
    // Workers/Edge: atob exists. atob throws InvalidCharacterError on
    // non-base64 input — re-throw with context so a corrupted file fails
    // cold-start with a recoverable message rather than the opaque
    // browser-style error.
    bin = atob(b64);
  } catch (err) {
    const msg = err instanceof Error ? err.message : String(err);
    throw new Error(`inter-regular.ts base64 decode failed: ${msg}`);
  }
  const out = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) out[i] = bin.charCodeAt(i);
  return out;
}

/** TTF magic byte sets — covers TrueType (0x00010000), OpenType ("OTTO"),
 *  and the legacy "true"/"typ1" sfnt versions. Anything else is not a
 *  shapeable font file and Satori would reject it later anyway. */
function looksLikeTtf(bytes: Uint8Array): boolean {
  if (bytes.byteLength < 4) return false;
  const [a, b, c, d] = bytes;
  // 00 01 00 00
  if (a === 0x00 && b === 0x01 && c === 0x00 && d === 0x00) return true;
  // "OTTO"
  if (a === 0x4f && b === 0x54 && c === 0x54 && d === 0x4f) return true;
  // "true"
  if (a === 0x74 && b === 0x72 && c === 0x75 && d === 0x65) return true;
  // "typ1"
  if (a === 0x74 && b === 0x79 && c === 0x70 && d === 0x31) return true;
  return false;
}

const decoded = decodeBase64(FONT_BASE64_PARTS.join(''));
if (!looksLikeTtf(decoded)) {
  // Fail loud at module instantiation so the issue is the cold-start
  // signature, not a confusing "Satori couldn't parse font" error 50ms later.
  throw new Error(
    `inter-regular.ts: decoded ${decoded.byteLength} bytes do not start with a TTF/OTF signature`,
  );
}

